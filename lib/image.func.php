<?php
require_once 'string.func.php';
function verifyImage($width,$height,$sess_name="verify",$type=3,$length=4,$pixel=30,$line=5){
    $image=imagecreatetruecolor($width,$height);
    $white=imagecolorallocate($image,255,255,255);
    $black=imagecolorallocate($image,0,0,0);
    imagefilledrectangle($image,0,0,$width,$height,$white);
    $chars=bulidRandomString($type,$length);
    $_SESSION[$sess_name]=$chars;
    $fontfiles=array("Deng.ttf","Dengb.ttf","Dengl.ttf");
    for($i=0;$i<$length;$i++){
        $size=mt_rand(20,23);
        $angle=mt_rand(-15,15);
        $x=25+$i*$size;
        $y=mt_rand(25,35);
        $fontfile="../fonts/".$fontfiles[mt_rand(0,count($fontfiles)-1)];
        $text=substr($chars,$i,1);
        $color=imagecolorallocate($image,mt_rand(50,90),mt_rand(80,200),mt_rand(90,180));
        imagettftext($image,$size,$angle,$x,$y,$color,$fontfile,$text);
    }
    if($pixel){
        for($i=0;$i<$pixel;$i++){
            imagesetpixel($image,mt_rand(0,$width-1),mt_rand(0,$height-1),$black);
        }
    }
    if($line){
        for($i=0;$i<$line;$i++){
            $color=imagecolorallocate($image,mt_rand(50,90),mt_rand(80,200),mt_rand(90,180));
            imageline($image,mt_rand($width*0.2,$width*0.8),mt_rand(0.2*$height,0.8*$height),mt_rand(0.2*$width,0.8*$width),mt_rand($height*0.2,$height*0.8),$color);
        }
    }
    ob_clean();
    header("content-type:image/gif");
    imagegif($image);
    imagedestroy($image);
}